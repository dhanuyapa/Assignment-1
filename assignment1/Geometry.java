public class Geometry {
public double getAreaOfCircle(double radius) {
	double area=3.14*radius*radius;
	return area;
}

public double geAresOfRec(double length,double height) {
	double areaR=length*height;
	return areaR;
}

public double getAreaTrangle(double base,double heightt) {
	double areaT=(1/2)*base*heightt;
	return  areaT;
}
}

